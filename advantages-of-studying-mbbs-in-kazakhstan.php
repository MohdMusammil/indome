                            <p class="para">Kazakhstan, with its capital in Astana and a population of approximately 1.9 crores, is a nation rich in diversity and culture. The word "Kazakh" itself signifies "wanderer," a testament to the country's nomadic heritage. This vast land also boasts the longest land border globally, shared with its neighbor, Russia, although it lacks access to the ocean. Russian and Kazakh are the most widely spoken languages in this diverse land.</p><br>
